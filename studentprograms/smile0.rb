# smile2.rb

d0 =  1000000000000000000000000000
d1 =  1000000000110000110000000000
d2 =  1000000000110000110000000000
d3 =  1000000000000000000000000000
d4 =  1000001100000000000011000000
d5 =  1000000110000000000110000000
d6 =  1000000011000000001100000000
d7 =  1000000000111111110000000000
d8 =  1000000000000000000000000000

t = 0
while t < 30
  puts(d0)
  puts(d1)
  puts(d2)
  puts(d3)
  puts(d4)
  puts(d5)
  puts(d6)
  puts(d7)
  puts(d8)
  puts()     # one blank line
  sleep(0.1) # pause 0.1 second

  d0 = (d0 - d0) / 10 + d0
  d1 = (d1 - d0) / 10 + d0
  d2 = (d2 - d0) / 10 + d0
  d3 = (d3 - d0) / 10 + d0
  d4 = (d4 - d0) / 10 + d0
  d5 = (d5 - d0) / 10 + d0
  d6 = (d6 - d0) / 10 + d0
  d7 = (d7 - d0) / 10 + d0
  d8 = (d8 - d0) / 10 + d0

  t = t + 1
end
